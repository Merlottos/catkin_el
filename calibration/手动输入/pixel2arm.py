# 使用最小二乘法求解变换矩阵 A
# 我们需要将 V 转置以适应 np.linalg.lstsq 的输入格式
# 转置后的 V.T 形状为 (3, n) 而 V_prime.T 形状为 (3, n)
A, residuals, rank, s = np.linalg.lstsq(V, V_prime, rcond=None)

# A 是变换矩阵
print(f"残差: {residuals}, 秩: {rank}, 奇异值: {s}")
print("变换矩阵 A:")
print(A)
print()

# 验证结果
V_prime_calculated = V @ A
# 转换为非科学计数法4位小数
V_prime_calculated_display = np.around(V_prime_calculated, decimals=2)
print("计算得到的新坐标系中的点:")
print(V_prime_calculated_display)
print()

# 比较计算得到的点和实际的新坐标系中的点
V_prime_display = np.around(V_prime, decimals=2)
print("实际的新坐标系中的点:")
